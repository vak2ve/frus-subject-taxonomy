def load_candidates():
    """Load and merge candidates from both discovery tiers.

    Filters out countries and organizations — those are outside the scope
    of the subject taxonomy. The raw JSON files retain them for future use
    in a places/organizations index.
    """
    candidates = []

    # Tier 2: Index candidates
    if os.path.exists(INDEX_CANDIDATES_FILE):
        with open(INDEX_CANDIDATES_FILE) as f:
            data = json.load(f)
        for i, c in enumerate(data.get('candidates', [])):
            candidates.append({
                'id': f'idx-{i:04d}',
                'term': c['term'],
                'source': 'index',
                'type': c.get('type', 'topic'),
                'volume_count': c.get('volume_count', 0),
                'doc_count': c.get('doc_count', 0),
                'volumes': c.get('volumes', []),
                'sub_entries': c.get('sub_entries', []),
                'variants': c.get('variants', []),
                'normalized': c.get('normalized', ''),
            })

    # Tier 3: LCSH candidates
    if os.path.exists(LCSH_CANDIDATES_FILE):
        with open(LCSH_CANDIDATES_FILE) as f:
            data = json.load(f)
        for i, c in enumerate(data.get('candidates', [])):
            candidates.append({
                'id': f'lcsh-{i:04d}',
                'term': c['term'],
                'source': 'lcsh',
                'type': 'topic',
                'source_count': c.get('source_count', 0),
                'source_terms': c.get('source_terms', []),
                'lcsh_uri': c.get('lcsh_uri', ''),
                'parent_label': c.get('parent_label', ''),
            })

    # Filter out types outside the subject taxonomy scope
    before = len(candidates)
    candidates = [c for c in candidates if c['type'] not in EXCLUDED_TYPES]
    type_excluded = before - len(candidates)
    if type_excluded:
        print(f"  Filtered out {type_excluded} country/organization entries (kept in raw JSON)")

    # Filter out remaining person names that slipped through discovery heuristics
    before = len(candidates)
    candidates = [c for c in candidates if not _looks_like_person(c['term'])]
    person_excluded = before - len(candidates)
    if person_excluded:
        print(f"  Filtered out {person_excluded} person-name entries (kept in raw JSON)")

    # Filter out malformed entries (embedded doc numbers, excessive whitespace)
    before = len(candidates)
    candidates = [c for c in candidates
                  if not re.search(r',\s*\d{2,3},\s*\n', c['term'])]
    malformed_excluded = before - len(candidates)
    if malformed_excluded:
        print(f"  Filtered out {malformed_excluded} malformed entries")

    return candidates


def load_category_candidates(category):
    """Load pre-split candidates from a category JSON file.

    These files are generated by split_review_categories.py.
    """
    filepath = CATEGORY_FILES[category]
    if not os.path.exists(filepath):
        print(f"  WARNING: {filepath} not found — run split_review_categories.py first")
        return []

    with open(filepath) as f:
        data = json.load(f)

    candidates = data.get('candidates', [])
    print(f"  Loaded {len(candidates)} {category} from {filepath}")
    return candidates


def load_taxonomy_categories():
    """Load category structure from taxonomy XML."""
    tree = etree.parse(TAXONOMY_FILE)
    root = tree.getroot()

    categories = {}
    for cat in root.findall('.//category'):
        cat_label = cat.get('label', '')
        subcats = set()
        for subcat in cat.findall('.//subcategory'):
            subcats.add(subcat.get('label', ''))
        categories[cat_label] = sorted(subcats)

    # Also collect all existing subject names for merge-target list
    subjects = []
    for subj in root.findall('.//subject'):
        name_el = subj.find('name')
        if name_el is not None and name_el.text:
            ref = subj.get('ref', '')
            subjects.append({'ref': ref, 'name': name_el.text})

    subjects.sort(key=lambda s: s['name'].lower())
    return categories, subjects


