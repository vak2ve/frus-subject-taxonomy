def build_category_html(category, categories, subjects):
    """Build an HTML review file for a single category."""
    candidates = load_category_candidates(category)
    if not candidates:
        return

    title = CATEGORY_TITLES[category]
    state_key = CATEGORY_STATE_KEYS[category]
    output_file = CATEGORY_OUTPUT_FILES[category]

    print(f"Building {category} HTML ({len(candidates)} candidates)...")
    html = build_html(candidates, categories, subjects,
                      title=title, state_key=state_key)

    with open(output_file, 'w', encoding='utf-8') as f:
        f.write(html)
    print(f"  Wrote {output_file} ({len(html):,} bytes)")


def main():
    parser = argparse.ArgumentParser(
        description='Build candidate review HTML files.')
    parser.add_argument(
        '--category',
        choices=['persons', 'organizations', 'topics', 'all'],
        default=None,
        help='Build a category-specific review file. '
             'Use "all" to build all three. '
             'Omit to build the combined legacy file.')
    args = parser.parse_args()

    print("Loading taxonomy categories...")
    categories, subjects = load_taxonomy_categories()
    print(f"  {len(categories)} categories, {len(subjects)} existing subjects")

    if args.category == 'all':
        # Build all three category-specific files
        for cat in ('persons', 'organizations', 'topics'):
            build_category_html(cat, categories, subjects)
    elif args.category:
        # Build a single category file
        build_category_html(args.category, categories, subjects)
    else:
        # Legacy combined mode
        print("Loading candidates (combined)...")
        candidates = load_candidates()
        print(f"  {len(candidates)} total candidates")

        index_count = sum(1 for c in candidates if c['source'] == 'index')
        lcsh_count = sum(1 for c in candidates if c['source'] == 'lcsh')
        print(f"  {index_count} from Index (Tier 2), {lcsh_count} from LCSH (Tier 3)")

        print("Building HTML...")
        html = build_html(candidates, categories, subjects)

        with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
            f.write(html)
        print(f"Wrote {OUTPUT_FILE} ({len(html):,} bytes)")


if __name__ == '__main__':
    main()
